    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        Disciplinas d = new Disciplinas();
        d.setHorario(null);
        d.setDisciplina(txtDisciplina.getText());
        d.setDia(Integer.parseInt(txtDia.getText()));
        
        String[] horario = txtHorario.getText().split(":");
        
        Calendar c = Calendar.getInstance();
        
        c.set(1, 1, 1970, Integer.parseInt(horario[0]), Integer.parseInt(horario[1]));
        
        d.setHorario(c.getTime());
        
        controller.create(d);
        tTable.setModel(new Table<>(controller.findDisciplinasEntities(), colums));

    }//GEN-LAST:event_btSalvarActionPerformed

    
