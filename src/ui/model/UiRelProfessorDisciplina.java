    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ItemSelection<Funcionarios> f = (ItemSelection<Funcionarios>) jcProfessor.getSelectedItem();
        ItemSelection<Disciplinas> d = (ItemSelection<Disciplinas>) jcDisciplina.getSelectedItem();
        
        List<Disciplinas> disciplinases = f.get().getDisciplinasList();
        for (Disciplinas disciplinasList : disciplinases) {
            if( d.get().getHorario().equals(disciplinasList.getHorario())
                    && Objects.equals(d.get().getDia(), disciplinasList.getDia()) ) {
                
                JOptionPane.showMessageDialog(rootPane, "Horário não disponível para este professor");
                return;
            }
        }
        
        Funcionarios f2 = f.get();
        List<Disciplinas> d2 = f2.getDisciplinasList();
        d2.add(d.get());
        
        f2.setDisciplinasList(d2);
        
        try {
            MainFrame.FUNCIONARIO_CONTROLLER.edit(f2);
        } catch (Exception ex) {
            Logger.getLogger(UiRelProfessorDisciplina.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tAulas.setModel(new Table<>(d2, colums));
    }//GEN-LAST:event_jButton1ActionPerformed

