    public UiRelProfessorDisciplina() {
        initComponents();
        
        // Fill Func
        List<Funcionarios> funcionarioses = MainFrame.FUNCIONARIO_CONTROLLER.findFuncionariosEntities();
        for (Funcionarios funcionarios : funcionarioses) {
            if( funcionarios.getProfessor() == 1 ) {
                jcProfessor.addItem(new ItemSelection<>(funcionarios)); 
            }
        }
        
        // Fill Disc
        List<Disciplinas> disciplinases2 = MainFrame.DISCIPLINA_CONTROLLER.findDisciplinasEntities();
        for (Disciplinas disciplinas : disciplinases2) {
            jcDisciplina.addItem(new ItemSelection<>(disciplinas)); 
        }
        
        
        tAulas.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent me) {
                ItemSelection<Funcionarios> f = (ItemSelection<Funcionarios>) jcProfessor.getSelectedItem();
                
                List<Disciplinas> disciplinases = f.get().getDisciplinasList();
                Funcionarios f2 = f.get();
                
                JTable table = (JTable) me.getSource();
                Point p = me.getPoint();
                int row = table.rowAtPoint(p);
                if (me.getClickCount() == 2) {
                    List<Disciplinas> n = new ArrayList<>();
                    int i = 0;

                    for (Disciplinas n1 : disciplinases) {
                        if( i++ != row ) {
                            n.add(n1);
                        } 
                    }
                    
                    f2.setDisciplinasList(n);
                    try {
                        MainFrame.FUNCIONARIO_CONTROLLER.edit(f2);
                    } catch (Exception ex) {
                        Logger.getLogger(UiRelProfessorDisciplina.class.getName()).log(Level.SEVERE, null, ex);
                    }
                                        
                    tAulas.setModel(new Table<>(n, colums));
                }
            }
        });
    }

